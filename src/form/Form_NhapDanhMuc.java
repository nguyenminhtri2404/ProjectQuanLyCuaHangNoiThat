    private void btn_LuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_LuuActionPerformed
         // TODO add your handling code here:
    // ẩn nút lưu, bật thêm xóa sửa,
        DanhMuc danhmuc = convertTextbox_to_Obj();

        //kiểm tra xem các textbox có rỗng không
        if (txt_tenDM.getText().isEmpty() || txt_tenDM.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else {
                if (flag == 1) {
                danhmuc.setMaloai(txt_MaDanhMuc.getText());
                dm.themDanhMuc(danhmuc);
            } else if (flag == 2) {
                // Cập nhật mã danh mục từ textbox, dù nó bị mờ
                danhmuc.setMaloai(txt_MaDanhMuc.getText());
                dm.capNhatDanhMuc(danhmuc);
            } else if (flag == 3) {
                int kq= JOptionPane.showConfirmDialog(this,"Bạn có muốn xóa không?","Thông báo",JOptionPane.YES_NO_OPTION,JOptionPane.YES_NO_CANCEL_OPTION);
                if(kq==JOptionPane.YES_OPTION) {
                    dm.xoaDanhMuc(danhmuc);
                    //vitri = vitri - 1;
                }
            }

                flag = 0;
                dm = new DSDanhMuc();
                dsDanhMuc = dm.layDanhSachDanhMuc();
                hienThiTextBox(vitri);
                hienThiTable();

                btn_Luu.setEnabled(false);
                btn_Them.setEnabled(true);
                btn_Sua.setEnabled(true);
                btn_Xoa.setEnabled(true);
        }
    }//GEN-LAST:event_btn_LuuActionPerformed

