    private void RangBuocText() {
        txt_tenDM.addFocusListener(new java.awt.event.FocusAdapter() {
            public void focusLost(java.awt.event.FocusEvent evt) {
                String tenDM = txt_tenDM.getText().trim();
                if (txt_tenDM.getText().isEmpty() && tenDM.length() <= tenDanhMucMaxLength) {
                    JOptionPane.showMessageDialog(null, "Tên không được để trống và không vượt quá " + tenDanhMucMaxLength + " ký tự.", "Thông báo", JOptionPane.WARNING_MESSAGE);
                    txt_tenDM.setText("");
                    txt_tenDM.requestFocus();
                }
            }
        });
    }
    private void hienThiComboboxSP(ArrayList<CuaHang> ch, JComboBox<String> cbo){
        for (CuaHang chang :ch)
        {
            cbo.addItem(chang.getTench());
        }
    }
    int TimViTriCuaHang(ArrayList<CuaHang> ch , String giatri){
        for (int i = 0 ;i<ch.size();i++)
            if(ch.get(i).getMach().equalsIgnoreCase(giatri))
                return i;
        return -1;
    }
    private void hienThiTextBox(int vitri)
    {
        DanhMuc n = dsDanhMuc.get(vitri);
        txt_MaDanhMuc.setText(n.maloai);
        txt_tenDM.setText(n.Tenloai);
        String mach = n.getMach();
        int vt = TimViTriCuaHang(dsCuaHang, mach);
        if(vt!= -1 )
        cbo_TenCuaHang.setSelectedIndex(vt);
    }
    
    private void hienThiTable()
    {
        String []Header = {"Mã Danh Mục","Tên Danh Mục","Mã Cửa Hàng"};
        DefaultTableModel model = new DefaultTableModel(Header,0);
        for(DanhMuc n: dsDanhMuc)
        {   
            Object [] chnt = {n.maloai,n.Tenloai,n.mach};
            model.addRow(chnt);
        }
        tbl_DanhMucSP.fixTable(jScrollPane1);
        tbl_DanhMucSP.setModel(model);
        tbl_DanhMucSP.setRowSelectionInterval(vitri, vitri);
           
    }
    
